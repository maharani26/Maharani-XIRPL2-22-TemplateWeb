  &emsp;&emsp;Tahun ini, pameran CASA Indonesia 2019 akan segera digelar kembali dalam beberapa pekan tepatnya pada tanggal 31 Mei hingga 3 Juni 2019 mendatang. Kembali menempati ballroom hotel The Ritz-Carlton Jakarta, Pacific Place, pameran CASA Indonesia 2019 akan menjadi penyelenggaraan yang ke sembilan sekaligus merayakan satu dekade penerbitan majalah CASA Indonesia. Sederet rangkain acara tengah disiapkan untuk menjamu para pengunjung seperti malam final CASA Design Challenge 2019, berbagai seminar, talkshow, sudut Architectural Project, area Cook & Design, area Mi Casa, dan tentunya adalah Designer's Showcase.
  <br>
&emsp;&emsp;Area Designer's Showcase diklaim menjadi salah satu magnet bagi pengunjung yang bertandang ke pameran CASA Indonesia. Tidak hanya menampilkan instalasi yang instagrammable, Designer's Showcase yang menyambut para pengunjung di sepanjang foyer ballroom merupakan ladang edukasi mengenai teknik arsitektur dan desain interior. Tidak jarang, para pengunjung yang hadir merupakan pelajar arsitektur maupun desain serta para penggiat desain lainnya. Secara langsung, pengunjung dapat berbincang dengan para arsitek maupun desainer interior yang menampilkan karyanya pada instalasi Designer's Showcase.
  <br>
